#define MAJOR_VERSION   "P2DFFT/V5" 

//
// Constants
//

#define DEBUG   0       // Set to 1 to get debugging ouptut

//
//  MAX_DIM sets the size of memory allocated for the data matrices, so
//   in effect is the maximum size of any dimension of the image file.
//

#define MAX_DIM 2048

//
//  MAX_FILES set the number of file arguments that can be specified.
//

#define MAX_FILES   1024

//
//  DIM_RAD and DIM_THT are the number of radial slices and number of angular
//    slices used in the algorithm to map the Cartesian values to polar.
//

#define DIM_THT 1024
#define DIM_RAD 2048

//
//  The starting and ending number of modes (spiral arms) to calculate
//

#define M_INI   0
#define M_FIN   6

//
//  The starting and ending FFT frequency values
//

#define FREQ_START  -50.0
#define FREQ_END     50.0

//
//  Permitted range of values for p2dfft -f|--fixed option
//

#define MIN_WINDOW  25
#define MAX_WINDOW  125

//
//  Limits for image size in any FITS file generated by a program using the
//    classes in this package
//

#define MIN_FITS    1 
#define MAX_FITS    MAX_DIM

//
//  Math constants
//

#define STEP_P  0.25
#define PI      3.1415926
#define GR_RAD  (PI/180.0)

//
//  Maximum arguments
//

#define MAX_ARGS    64

